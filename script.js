  /* ---------- auto-restore from device via File System Access API ---------- */

  function maybeAutoRestore() {
    var key = activeKey();
    if (!key || audioMemory[key]) return;
    var task = getTask(key);
    if (!task || !task.completed || !task.savedWithHandle) return;
    if (!HAS_FS_ACCESS) return;

    state.restoring = true;
    render();
    idbGet(key).then(function (handle) {
      if (!handle) { state.restoring = false; render(); return; }
      return handle.queryPermission({ mode: 'read' }).then(function (perm) {
        if (perm !== 'granted') { state.restoring = false; render(); return; }
        return handle.getFile().then(function (file) {
          var url = URL.createObjectURL(file);
          audioMemory[key] = { url: url, seconds: task.seconds || 0 };
          state.restoring = false;
          render();
        });
      });
    }).catch(function () {
      state.restoring = false;
      render();
    });
  }

  /* ---------- recording ---------- */

  function stopStream(recorder) {
    if (recorder && recorder.stream) recorder.stream.getTracks().forEach(function (tr) { tr.stop(); });
  }

  var MIME_CANDIDATES = [
    'audio/mp4',
    'audio/webm;codecs=opus',
    'audio/webm',
    'audio/aac',
    'audio/ogg;codecs=opus'
  ];

  function pickSupportedMimeType() {
    if (typeof MediaRecorder === 'undefined' || !MediaRecorder.isTypeSupported) return '';
    for (var i = 0; i < MIME_CANDIDATES.length; i++) {
      if (MediaRecorder.isTypeSupported(MIME_CANDIDATES[i])) return MIME_CANDIDATES[i];
    }
    return '';
  }

  function extensionForMimeType(mimeType) {
    if (!mimeType) return 'webm';
    if (mimeType.indexOf('mp4') !== -1) return 'm4a';
    if (mimeType.indexOf('aac') !== -1) return 'aac';
    if (mimeType.indexOf('ogg') !== -1) return 'ogg';
    return 'webm';
  }

  function startRecording() {
    state.micError = null;
    state.recordWarning = '';
    var key = activeKey();
    if (!navigator.mediaDevices || !navigator.mediaDevices.getUserMedia) {
      state.micError = 'Микрофон недоступен в этом окружении. На реальном сайте запись идёт через микрофон устройства.';
      simulateRecording();
      return;
    }
    navigator.mediaDevices.getUserMedia({ audio: true }).then(function (stream) {
      state.audioChunks = [];
      var preferredMimeType = pickSupportedMimeType();
      var mr = preferredMimeType ? new MediaRecorder(stream, { mimeType: preferredMimeType }) : new MediaRecorder(stream);
      var actualMimeType = mr.mimeType || preferredMimeType || 'audio/webm';
      state.mediaRecorder = mr;
      mr.ondataavailable = function (e) { if (e.data && e.data.size > 0) state.audioChunks.push(e.data); };
      mr.onstop = function () {
        var blob = new Blob(state.audioChunks, { type: actualMimeType });
        audioMemory[key] = { url: URL.createObjectURL(blob), blob: blob, seconds: state.seconds, mimeType: actualMimeType };
        stopStream(mr);
        state.recording = false;
        render();
      };
      mr.start();
      state.recording = true;
      state.seconds = 0;
      render();
      state.timer = setInterval(recordTick, 1000);
    }).catch(function () {
      state.micError = 'Доступ к микрофону не разрешён. После разрешения запись сохраняется только у вас на устройстве.';
      simulateRecording();
    });
  }

  function recordTick() {
    state.seconds++;
    var el = document.getElementById('rec-status');
    if (el) el.textContent = 'Идёт запись… ' + fmt(state.seconds);
    if (state.recordWarning && state.seconds >= MIN_RECORD_SECONDS) {
      state.recordWarning = '';
      var warnEl = document.getElementById('record-warning');
      if (warnEl) warnEl.remove();
    }
  }

  function simulateRecording() {
    state.recording = true;
    state.seconds = 0;
    render();
    state.timer = setInterval(recordTick, 1000);
  }

  function stopRecording() {
    clearInterval(state.timer);
    var key = activeKey();
    if (state.mediaRecorder && state.mediaRecorder.state !== 'inactive') {
      state.mediaRecorder.stop();
    } else {
      state.recording = false;
      if (!audioMemory[key]) audioMemory[key] = { url: null, seconds: state.seconds, simulated: true };
      render();
    }
  }

