  var saveTimer = null;
  function saveStore() {
    if (!state.currentUser) return;
    clearTimeout(saveTimer);
    saveTimer = setTimeout(function () {
      db.collection('progress').doc(state.currentUser).set(STORE).catch(function (e) {
        console.error('Progress save failed', e);
      });
    }, 1500);
  }

